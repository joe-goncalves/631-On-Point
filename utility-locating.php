<h1>Utility Locating</h1>
<p>On Point Locating can save you time and money by locating buried utilities before you start your project. Buried utilities can include electric service, water service, sewer service, communications like phone and cable TV, and others. They may pass through any area where work or digging needs to be done—to install a fence, sprinkler system, or even to plant a tree. If you don’t scan for buried utilities, you risk hitting one by accident—risking your budget, your service, and your safety.</p>
<h2><em>"Your first call should be 811. Your second call should be us."</em></h2>
<p>811 is the "call before you dig" number, to mark out public utilities, free of charge to you. 811 does not cover private utilities, which are the responsibility of the property owner.</p>
<p>You should call 811 before every project, to have the public utilities marked out. This prevents costly damage for which you could be liable, and prevents hazards to safety and life that could be much worse.</p>
<p>Buried utilities are considered "private" if they are installed after a service meter. The free 811 number does not cover any private buried utilities on your property or project. Private utilities may be buried anywhere to supply pools/spas, gas generators and other gas services, pools/spas, or lighting and power for detached garages.</p>
